		internal func dangle(_ shouldDangle: Bool = true) -> MessageSendEventsProvider {
			self.dangling = shouldDangle
			return self
		}

		deinit {
			if Bindings.suspendFreedom || Self.suspendFreedom {
				return
			}

			if !self.dangling {
				if Self.enableDeinitLogging {
					Bindings.print(
